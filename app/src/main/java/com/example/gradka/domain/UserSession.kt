package com.example.gradka.domain

/**
 * Локально сохранённая сессия пользователя.
 *
 * Используется для восстановления состояния авторизации при повторном запуске приложения
 * без необходимости повторного ввода телефона и OTP-кода.
 *
 * @property phone Номер телефона аутентифицированного пользователя.
 * @property name Имя пользователя для отображения в профиле.
 */
data class UserSession(
    val phone: String,
    val name: String
)