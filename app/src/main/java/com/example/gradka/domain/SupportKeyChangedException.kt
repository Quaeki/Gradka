package com.example.gradka.domain

/**
 * Бросается, когда сервер вернул публичный ключ поддержки, отличающийся от закреплённого
 * (TOFU — trust on first use).
 *
 * Смена ключа может означать легитимную ротацию ключа оператора, но также и попытку
 * MITM-атаки со стороны скомпрометированного сервера, поэтому новый ключ никогда
 * не принимается автоматически. Пользователь подтверждает смену ключа явно —
 * очисткой чата, после которой новый ключ закрепляется заново.
 */
class SupportKeyChangedException : Exception("Support public key changed")
